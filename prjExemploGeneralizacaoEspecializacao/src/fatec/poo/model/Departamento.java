package fatec.poo.model;

/**
 *
 * @author WLLR9505
 */
public class Departamento {
    private String sigla;
    private String nome;

    public Departamento(String sigla, String nome) {
        this.sigla = sigla;
        this.nome = nome;
    }

    public String getSigla() {
        return sigla;
    }

    public String getNome() {
        return nome;
    }
    
}
